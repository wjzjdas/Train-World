//使HEX显示当前关卡序列号，
// 0 —— startscreen
// 1~10 —— 当前关卡
void write_hex(int value) {
    uint32_t hex_value = 0;

    for (int i = 0; i < 6; i++) {
        int digit = value % 10;
        hex_value |= ((uint32_t)HEX_LOOKUP[digit] << (i * 8));
        value /= 10;
        if (value == 0) break;
    }

    *(volatile uint32_t *)HEX_DISPLAY = hex_value;
}

int read_switches() {
    return *(volatile uint32_t *)SW_BASE;
}

