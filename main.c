void swap_buffers() {
    vp->fbp = 1;  // Request swap
    while ((vp->StatusControl & 1) != 0);  // Wait for swap to complete
}

void update_frame_counter() {
    frame_counter++;
}

//无论如何都要保持每帧刷新屏幕，这是因为后面会加入动画
void redraw_frame(const Position *chara_pos) {
    clear_screen();  // Clear once per frame
    // Draw background
    for (int y = 50; y < 200; y++) {
        for (int x = 20 + (current_level * 10); x < 300 - (current_level * 10); x++) {
            if (x >= 0 && x < WIDTH) {
                vp->bfbp->pixels[y][x] = GREEN;
            }
        }
    }
    for (int i = 0; i < 5; i++) {
        if (trains[i].active) draw_train(&trains[i]);
    }
    // Draw all elements in order
    if (current_level > 0) {
        draw_trigger(&trigger_pos[current_level]);
    }

    update_frame_counter();  // Must be called here
    draw_character(&slugfox);
    swap_buffers();  // Swap only once per frame
}

