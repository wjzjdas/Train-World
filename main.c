void init_video() {
    //设置分辨率为320*240（学校硬件如此）
    vp->resolution = (HEIGHT << 16) | WIDTH;
    // Initialize back buffer to our allocated array
    vp->bfbp = (struct fb_t volatile *)backbuffer.pixels;
}

void clear_screen() {
    for (int y = 0; y < HEIGHT; y++) {
        for (int x = 0; x < WIDTH; x++) {
            vp->bfbp->pixels[y][x] = BLACK;
        }
    }
}

void draw_start_screen() {
    clear_screen();
    //绘制startscreen标题
    for (int y = 100; y < 140; y++) {
        for (int x = 90; x < 230; x++) {
            vp->bfbp->pixels[y][x] = RED;
        }
    }
    //绘制玩家位置
    draw_character(&slugfox);
    write_hex(current_level);
    swap_buffers();
}

void draw_init_level() {
    clear_screen();  
    for (int y = 50; y < 200; y++) {
        for (int x = 20 + (current_level * 10); x < 300 - (current_level * 10); x++) {
            if (x >= 0 && x < WIDTH) {
                vp->bfbp->pixels[y][x] = GREEN;
            }
        }
    }
    draw_trigger(&trigger_pos[current_level]);
    draw_character(&slugfox);
    for (int i = 0; i < 5; i++) {
        if (trains[i].active) draw_train(&trains[i]);
    }
    write_hex(current_level);
    swap_buffers();
}

