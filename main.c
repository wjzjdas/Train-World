// Draw Slugfox with animation based on face_dir
void draw_character(Slugfox *slugfox) {
    int x = slugfox->chara_pos.x;
    int y = slugfox->chara_pos.y;
    pixel_rgb_t volatile (*sprite)[30][17];  // Pointer to the current sprite array
    
    // Animation frame selection (cycles every ~0.5 seconds, assuming 60 FPS)
    int frame_duration = 10;  // 30 frames = ~0.5 seconds at 60 FPS
    int anim_frame = (frame_counter / frame_duration) % 3;  // 0, 1, 2 cycle
    // Select sprite based on face_dir
    if (slugfox->face_dir == 1) {  // Facing right
        switch (anim_frame) {
            case 0:
                sprite = &slugfox->static_right_1;
                break;
            case 1:
                sprite = &slugfox->static_right_2;
                break;
            case 2:
                sprite = &slugfox->static_right_3;
                break;
            default:
                sprite = &slugfox->static_right_1;  // Fallback
        }
    } else {  // Facing left (face_dir == 0)
        switch (anim_frame) {
            case 0:
                sprite = &slugfox->static_left_1;
                break;
            case 1:
                sprite = &slugfox->static_left_2;
                break;
            case 2:
                sprite = &slugfox->static_left_3;
                break;
            default:
                sprite = &slugfox->static_left_1;  // Fallback
        }
    }

    // Draw the selected sprite, including black pixels
    for (int dy = 0; dy < slugfox->h; dy++) {
        for (int dx = 0; dx < slugfox->w; dx++) {
            int px = x + dx;
            int py = y + dy;
            pixel_rgb_t pixel = (*sprite)[dy][dx];
            // Draw all pixels within bounds, no transparency check
            if (px >= 0 && px < WIDTH && py >= 0 && py < HEIGHT) {
                vp->bfbp->pixels[py][px] = pixel;
            }
        }
    }
}

void draw_trigger(Position *trigg_pos) {
    // 5x5 red square for trigger (only for levels 1-9)
    if (current_level > 0 && current_level <= 10) {
        int tx = trigg_pos->x;
        int ty = trigg_pos->y;
        for (int dy = 0; dy < 5; dy++) {
            for (int dx = 0; dx < 5; dx++) {
                int px = tx + dx;
                int py = ty + dy;
                if (px >= 0 && px < WIDTH && py >= 0 && py < HEIGHT) {
                    vp->bfbp->pixels[py][px] = RED;
                }
            }
        }
    }
}

void trigger_collision(Position *chara_pos){
    if (current_level <= 9) {
        int tx = trigger_pos[current_level].x;
        int ty = trigger_pos[current_level].y;
        if (chara_pos->x + slugfox.w >= tx && chara_pos->x <= tx + slugfox.w &&
            chara_pos->y + slugfox.h >= ty && chara_pos->y <= ty + slugfox.h) {
            current_level++;
            if (current_level > 10) current_level = 10;
            //将玩家位置重设为关卡开始点
            *chara_pos = init_pos[current_level];
            draw_init_level(current_level);  // Redraw new level
            write_hex(current_level);   
        }
    }
}

void update_character_movement(Position *chara_pos) {
    int switches = read_switches();
    // SW2: Move up
    if (switches & 0x04) {
        if (chara_pos->y > 0) {  // Check top boundary
            chara_pos->y = chara_pos->y-3;
        }
    }
    // SW3: Move down
    if (switches & 0x08) {
        if (chara_pos->y + slugfox.h < HEIGHT) {  // Check bottom boundary (5x5 character)
            chara_pos->y = chara_pos->y+3;
        }
    }
    // SW4: Move left
    if (switches & 0x10) {
        if (chara_pos->x > 0) {  // Check left boundary
            chara_pos->x = chara_pos->x-3;
        }
        slugfox.face_dir = 0; //left
    }
    // SW5: Move right
    if (switches & 0x20) {
        if (chara_pos->x + slugfox.w < WIDTH) {  // Check right boundary (5x5 character)
            chara_pos->x = chara_pos->x+3;
        }
        slugfox.face_dir = 1;  // Right
    }
}


