int train_collision(const Position *chara_pos) {
    int chara_left = chara_pos->x;
    int chara_right = chara_pos->x + slugfox.w;
    int chara_top = chara_pos->y;
    int chara_bottom = chara_pos->y + slugfox.h;

    for (int i = 0; i < 5; i++) {
        if (trains[i].active) {
            int train_left = trains[i].pos.x;
            int train_right = trains[i].pos.x + trains[i].w;
            int train_top = trains[i].pos.y;
            int train_bottom = trains[i].pos.y + trains[i].h;

            if (chara_right >= train_left && chara_left <= train_right &&
                chara_bottom >= train_top && chara_top <= train_bottom) {
                return 1;  // Collision detected
            }
        }
    }
    return 0;  // No collision
}

void update_train(Train *train) {
    if (train->active) {
        train->pos.x += train->hor_speed;
        train->pos.y += train->ver_speed;
        if (train->pos.x <= 0) {
            train->pos.x = 0;
            train->hor_speed = -train->hor_speed;
        }
        else if (train->pos.x + train->w >= WIDTH) {
            train->pos.x = WIDTH - train->w;
            train->hor_speed = -train->hor_speed;
        }
        if (train->pos.y <= 0) {
            train->pos.y = 0;
            train->ver_speed = -train->ver_speed;
        }
        else if (train->pos.y + train->h >= HEIGHT) {
            train->pos.y = HEIGHT - train->h;
            train->ver_speed = -train->ver_speed;
        }
    }
}
void draw_train(const Train *train) {
    if (train->active) {
        int x = train->pos.x;
        int y = train->pos.y;
        for (int dy = 0; dy < train->h; dy++) {
            for (int dx = 0; dx < train->w; dx++) {
                int px = x + dx;
                int py = y + dy;
                if (px >= 0 && px < WIDTH && py >= 0 && py < HEIGHT) {
                    vp->bfbp->pixels[py][px] = BLUE;
                }
            }
        }
    }
}

